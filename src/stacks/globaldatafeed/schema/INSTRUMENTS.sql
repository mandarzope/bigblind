CREATE TABLE INSTRUMENTS (
    `ID` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT,
    `EXCHANGE` CHAR(255) NOT NULL,
    `EXPIRY` CHAR(255) NOT NULL,
    `IDENTIFIER` CHAR(255) NOT NULL,
    `INDEXNAME` CHAR(255) NOT NULL,
    `NAME` CHAR(255) NOT NULL,
    `OPTIONTYPE` CHAR(255) NOT NULL,
    `PRICEQUOTATIONUNIT` CHAR(255) NOT NULL,
    `PRODUCT` CHAR(255) NOT NULL,
    `PRODUCTMONTH` CHAR(255) NOT NULL,
    `STRIKEPRICE` INT(11) NOT NULL DEFAULT 0,
    `TRADESYMBOL` CHAR(255) NOT NULL,
    `UNDERLYINGASSET` CHAR(255) NOT NULL,
    `UNDERLYINGASSETEXPIRY` CHAR(255) NOT NULL,
    `QUOTATIONLOT` INT(11) NOT NULL DEFAULT 0,
    `LASTCRAWLEDFROM` DATETIME,
    `LASTCRAWLEDTO` DATETIME,
    PRIMARY KEY (`ID`),
    KEY `EXCHANGE` (`EXCHANGE`),
    KEY `EXPIRY` (`EXPIRY`),
    KEY `NAME` (`NAME`),
    KEY `OPTIONTYPE` (`OPTIONTYPE`),
    KEY `STRIKEPRICE` (`STRIKEPRICE`),
    KEY `TRADESYMBOL` (`TRADESYMBOL`),
    KEY `PRODUCT` (`PRODUCT`),
    KEY `PRODUCTMONTH` (`PRODUCTMONTH`),
    UNIQUE `IDENTIFIER` (`IDENTIFIER`)
 ) ENGINE=InnoDB DEFAULT CHARSET=utf8